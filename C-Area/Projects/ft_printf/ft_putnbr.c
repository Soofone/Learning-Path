#include "ft_printf.h"

int	ft_putnbr(int n)
{
	char	c;
	int		k;

	k = 0;
	if (n == -2147483648)
	{
		return (ft_putstr("-2147483648"));
	}
	else
	{
		if (n < 0)
		{
			k += ft_putchar('-');
			n = -n;
		}
		if (n >= 10)
			k += ft_putnbr(n / 10);
		c = (n % 10) + '0';
		k += ft_putchar(c);
	}
	return (k);
}
